/**
 * 获取热门股票 (东方财富-按成交额排序)
 */
export async function getHotStocks() {
  try {
    const { data } = await jsonRequest.get('/em-api/api/qt/clist/get', {
      params: {
        pn: 1,
        pz: 15,
        po: 1,
        np: 1,
        fltt: 2,
        invt: 2,
        fid: 'f6',
        fs: 'm:0+t:6,m:0+t:80,m:1+t:2,m:1+t:23',
        fields: 'f2,f3,f4,f5,f6,f8,f12,f14'
      }
    })
    if (!data?.data?.diff) return []
    return data.data.diff.map((item, idx) => ({
      rank: idx + 1,
      code: item.f12,
      name: item.f14,
      price: item.f2,
      changePercent: item.f3,
      change: item.f4,
      volume: item.f5,
      amount: item.f6,
      turnoverRate: item.f8,
      symbol: (String(item.f12).startsWith('6') ? 'sh' : 'sz') + item.f12
    }))
  } catch {
    return []
  }
}

