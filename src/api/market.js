/**
 * 解析新浪行情文本获取名称
 * 格式: var hq_str_sh600000="浦发银行,11.20,11.15,11.25,..."
 */
function parseSinaStockName(text, symbol) {
  const match = text.match(new RegExp(`var hq_str_${symbol}="([^,]+)`, 'i'))
  return match ? match[1] : null
}

/**
 * 批量获取股票名称（使用新浪API，一次最多800只）
 * @param {string[]} symbols - 股票代码列表
 */
export async function getStockNames(symbols) {
  if (!symbols || symbols.length === 0) return {}

  const names = {}
  const batchSize = 800 // 新浪API一次最多支持800只

  for (let i = 0; i < symbols.length; i += batchSize) {
    const batch = symbols.slice(i, i + batchSize)
    try {
      const { data } = await textRequest.get(`/sina-hq/list=${batch.join(',')}`)

      batch.forEach(symbol => {
        const name = parseSinaStockName(data, symbol)
        if (name) {
          names[symbol] = name
        }
      })
    } catch (error) {
      console.error('获取股票名称失败:', error)
    }
  }

  return names
}

